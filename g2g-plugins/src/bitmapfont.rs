/// Whether the glyph for `c` has any set pixel (a drawable, non-space glyph).
/// Used by tests; cheap enough to leave un-gated.
pub fn is_drawable(c: char) -> bool {
    glyph(c).iter().any(|&row| row != 0)
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn letters_digits_and_punct_have_glyphs() {
        for c in "ABCXYZ0189.,!?:-".chars() {
            assert!(is_drawable(c), "{c} should have a non-empty glyph");
        }
    }

    #[test]
    fn space_and_unknown_are_blank() {
        assert!(!is_drawable(' '));
        assert!(!is_drawable('\u{2603}')); // snowman: unsupported
    }

    #[test]
    fn lowercase_folds_to_uppercase() {
        assert_eq!(glyph('a'), glyph('A'));
        assert_eq!(glyph('z'), glyph('Z'));
    }
}
